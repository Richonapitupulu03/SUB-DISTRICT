          <br>Pelaporan kelahiran harus memenuhi persyaratan berikut ini. Namun karena adanya kondisi dan wilayah domisili yang berbeda-beda, maka nantinya mungkin juga ada perbedaan persyaratan yang diperlukan. Khusus di Ibu Kota, berdasarkan laman resmi Dukcapil Jakarta, pencatatan akta kelahiran dapat dilakukan di:<br>

          <br>
          1.Dinas<br>
          2.Suku Dinas<br>
          3.Seksi Dinas Kependudukan dan Pencatatan Sipil Kecamatan<br>
          4.Puskesmas Kecamatan<br>
          5.Rumah Sakit, dan<br>
          6.Loket pelayanan lainnya. <br>
          <br>
          Sesuai UU No. 24/2013 tentang Perubahan atas UU No. 23/2006, pencatatan akta kelahiran dilakukan pada instansi pelaksana sesuai dengan domisili pelapor. Biayanya gratis. Waktu pelayanan 5 hari kerja sejak tanggal diterimanya berkas persyaratan lengkap. Lokasi pelayanan di Kelurahan.<br>

          <br>Untuk memperoleh layanan pelaporan kelahiran harus memenuhi syarat berikut ini:<br>

