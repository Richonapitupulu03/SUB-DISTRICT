          <h3>LAYANAN PEMBUATAN KARTU KELUARGA</h3>

          <center><img src="../images/kk.png" style="width:500px;height:300px;"></center>
          <h6 style="color: black">
          <br>
          Berikut  cara membuat KK baru 2021 yang harus kamu lakukan:<br>
          <br>
          1. Meminta surat pengantar pembuatan kartu keluarga baru di Ketua RT setempat

          <br>
          2. Membawa surat pengantar tersebut ke Ketua RW dan meminta stempel RW.

          <br>
          3. Membawa surat pengantar tersebut dan persyaratan lainnya ke kantor kelurahan, lalu mengisi formulir permohonan pembuatan KK baru yang tersedia di kantor kelurahan

          <br>
          4. Petugas kelurahan akan mengecek kelengkapan berkas-berkas yang menjadi syarat pembuatan KK baru

          <br>
          5. Jika sudah lengkap, Lurah akan menandatangani formulir permohonan pembuatan KK baru tersebut

          <br>
          6. Berkas yang sudah ditandatangan Lurah kemudian kamu teruskan ke Kecamatan<br>
          <br>
          Syarat Pembuatan KK(KARTU KELUARGA):<br>
          <br>
          1. Pasangan yang Baru Menikah
          Untuk pasangan yang baru menikah, Kartu Keluarga dapat dibuat setelah pernikahan selesai dilakukan. <br>

          <br>
          Dengan memiliki KK, kamu jadi bisa mengurus berbagai keperluan lebih mudah. Misalnya saat kamu ingin membeli rumah dengan  Kredit Pepemilikan Rumah (KPR), membuat tabungan, paspor dan lainnya. <br>

          <br>
          Berikut syarat yang harus dipenuhi untuk membuat Kartu Keluarga:<br>

          <br>
          - Surat pengantar dari RT yang sudah distempel RW

          <br>
          - Fotocopy buku nikah atau akta perkawinan

          <br>
          - Surat keterangan pindah (khusus untuk anggota keluarga pendatang)<br>

          <br>
          2. Penambahan Anggota Keluarga (Kelahiran)
          Jika terdapat anggota keluarga yang baru lahir, maka kamu wajib menambahkannya ke dalam Kartu Keluarga. <br>

          <br>
          Berikut beberapa syarat yang harus kamu lengkapi untuk menambahkan anggota pada KK:<br>

          <br>
          - Surat pengantar dari RT atau RW

          <br>
          - Kartu keluarga (KK) yang lama

          <br>
          - Surat keterangan kelahiran anggota keluarga baru yang akan ditambahkan ke dalam kartu keluarga nanti<br>

          <br>
          3. Penambahan Anggota Keluarga yang Menumpang
          Tak hanya yang lahir,, kamu juga bisa menambahkan anggota keluarga yang menumpang di Kartu Keluarga. <br>

          <br>
          Berikut syarat-syarat yang harus disiapkan:<br>

          <br>
          - Surat pengantar RT atau RW daerah setempat

          <br>
          - Kartu keluarga yang lama atau kartu keluarga yang akan ditumpangi

          <br>
          - Surat keterangan pindah datang (jika tidak satu daerah)

          <br>
          - Surat keterangan datang dari luar negeri (untuk WNI yang datang dari luar negeri)

          <br>
          - Paspor, izin tinggal tetap, dan surat keterangan catatan kepolisian (SKCK) atau surat tanda lapor diri (untuk WNA)<br>

          <br>
          4. Pengurangan Anggota Keluarga (Kematian)
          Jika ada ada pengurangan anggota keluarga karena kematian, maka kamu wajib mengubah data di Kartu Keluargamu. <br>

          <br>
          Beberapa persyaratan tersebut diantaranya, yaitu :<br>

          <br>
          - Surat pengantar dari RT atau RW daerah setempat
          <br>
          - Kartu keluarga (KK) yang lama

          <br>
          - Surat keterangan kematian (untuk yang meninggal dunia)

          <br>
          - Jika pengurangan terjadi karena adanya anggota keluarga yang pindah, maka surat keterangan kematian diganti dengan surat keterangan pindah (untuk anggota keluarga yang pindah).<br>

          <br>
          5. Penggantian Karena Hilang atau Rusak
          Adakala Kartu Keluarga hilang atau rusak karena suatu hal sehingga kamu terpaksa untuk menggantinya dengan yang baru. <br>

          <br>
          Kamu bisa mengurusnya dengan menyiapkan beberapa syarat di bawah ini:<br>

          <br>
          - Surat pengantar dari RT atau RW daerah setempat

          <br>
          - Surat keterangan kehilangan dari pihak kepolisian (hanya untuk KK yang hilang)

          <br>
          - Kartu keluarga (KK) yang rusak (hanya untuk KK yang rusak)

          <br>
          - Fotocopy dokumen kependudukan (KTP) dari salah satu anggota keluarga

          <br>
          - Dokumen keimigrasian bagi orang asing (WNA)
