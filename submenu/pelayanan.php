          2. Asli dan fotokopi KK bagi penduduk/SKSKPNP bagi penduduk non-permanen<br>
          3. Asli dan fotokopi KTP orangtua/SKDS/Surat Keterangan Pelaporan Tamu<br>
          4. Asli dan fotokopi Surat Nikah/Akta Perkawinan Orangtua<br>
          5. Asli dan fotokopi paspor bagi warga negara asing<br>
          6. Surat Keterangan Kepolisian untuk anak yang tidak diketahui asal-usulnya, dan
